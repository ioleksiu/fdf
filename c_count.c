#include "fdf.h"

int			c_count(char *s)
{
	int		i;

	i = 0;
	if (!s)
		ft_error();
	while (*s == ' ')
		s++;
	while (*s)
	{
		if (*s == ' ')
		{
			while (*s == ' ')
				s++;
			if (*s)
				i++;
		}
		s++;
	}
	return (i);
}
